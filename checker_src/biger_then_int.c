int		w_min(char *num)
{
	num++;
	if (num[0] > '2' || (num[0] == '2' && num[1] > '1') ||
		(num[0] == '2' && num[1] == '1' && num[2] > '4')
		|| (num[0] == '2' && num[1] == '1' && num[2] == '4' && num[3] > '7')
		|| (num[0] == '2' && num[1] == '1' && num[2] == '4'
			&& num[3] == '7' && num[4] > '4')
		|| (num[0] == '2' && num[1] == '1' && num[2] == '4'
			&& num[3] == '7' && num[4] == '4' && num[5] > '8')
		|| (num[0] == '2' && num[1] == '1' && num[2] == '4'
			&& num[3] == '7' && num[4] == '4' && num[5] == '8'
			&& num[6] > '3')
		|| (num[0] == '2' && num[1] == '1' && num[2] == '4'
			&& num[3] == '7' && num[4] == '4' && num[5] == '8'
			&& num[6] == '3' && num[7] > '6')
		|| (num[0] == '2' && num[1] == '1' && num[2] == '4'
			&& num[3] == '7' && num[4] == '4' && num[5] == '8'
			&& num[6] == '3' && num[7] == '6' && num[8] > '4')
		|| (num[0] == '2' && num[1] == '1' && num[2] == '4'
			&& num[3] == '7' && num[4] == '4' && num[5] == '8'
			&& num[6] == '3' && num[7] == '6' && num[8] == '4'
			&& num[9] > '8'))
		return (0);
	return (1);
}

